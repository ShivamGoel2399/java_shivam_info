package static_nonstatic_eg;

public class Calculator {

	
	public static void iamStatic() {
		System.out.println("I am static method of Calculator classs");
	}
	
	public  void iamNonStatic() {
		System.out.println("I am non static method of Calculator classs");
	}
	public  void iamNonStaticAgain() {
		System.out.println("I am non static method again of Calculator classs");
	}
}
